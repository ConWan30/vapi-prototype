/**
 * deploy-resume-core.js — Resume core deployment after funding top-up
 *
 * Picks up from step 4/6 using already-deployed addresses from env vars.
 * Deploys: SkillOracle, ProgressAttestation, TeamProofAggregator
 * Then grants reputation-updater roles and writes bridge/.env.testnet.
 *
 * Usage (after topping up IOTX from https://faucet.iotex.io):
 *   cd contracts
 *   DEVICE_REGISTRY_ADDRESS=0x73753E916eEE0DAfEc1f6a16d98d4E4aFf9E8230 \
 *   POAC_VERIFIER_ADDRESS=0x26178AD95DB507f0D298fAAFC19752fC86166c6C \
 *   BOUNTY_MARKET_ADDRESS=0x6B288EfbC26cDBd02d1e3735299274597242315E \
 *     npx hardhat run scripts/deploy-resume-core.js --network iotex_testnet
 */

const hre  = require("hardhat");
const fs   = require("fs");
const path = require("path");

async function main() {
  const [deployer] = await hre.ethers.getSigners();
  const balance    = await hre.ethers.provider.getBalance(deployer.address);

  console.log("=== VAPI Core Deployment RESUME (steps 4-6) ===");
  console.log(`Network:  ${hre.network.name}`);
  console.log(`Deployer: ${deployer.address}`);
  console.log(`Balance:  ${hre.ethers.formatEther(balance)} IOTX`);
  console.log("");

  // --- Load already-deployed addresses ---
  const registryAddr  = process.env.DEVICE_REGISTRY_ADDRESS || "";
  const verifierAddr  = process.env.POAC_VERIFIER_ADDRESS   || "";
  const marketAddr    = process.env.BOUNTY_MARKET_ADDRESS   || "";

  if (!registryAddr)  throw new Error("DEVICE_REGISTRY_ADDRESS required");
  if (!verifierAddr)  throw new Error("POAC_VERIFIER_ADDRESS required");
  if (!marketAddr)    throw new Error("BOUNTY_MARKET_ADDRESS required");

  console.log("Using already-deployed:");
  console.log(`  TieredDeviceRegistry: ${registryAddr}`);
  console.log(`  PoACVerifier:         ${verifierAddr}`);
  console.log(`  BountyMarket:         ${marketAddr}`);
  console.log("");

  const maxTimestampSkew = process.env.MAX_TIMESTAMP_SKEW || 3600;
  const platformFeeBps   = process.env.PLATFORM_FEE_BPS   || 250;

  // --- 4. Deploy SkillOracle ---
  console.log("4/6 Deploying SkillOracle...");
  const SkillOracle = await hre.ethers.getContractFactory("SkillOracle");
  const oracle = await SkillOracle.deploy(verifierAddr);
  await oracle.waitForDeployment();
  const oracleAddr = await oracle.getAddress();
  console.log(`     SkillOracle deployed at: ${oracleAddr}`);

  // --- 5. Deploy ProgressAttestation ---
  console.log("5/6 Deploying ProgressAttestation...");
  const ProgressAttestation = await hre.ethers.getContractFactory("ProgressAttestation");
  const progress = await ProgressAttestation.deploy(verifierAddr);
  await progress.waitForDeployment();
  const progressAddr = await progress.getAddress();
  console.log(`     ProgressAttestation deployed at: ${progressAddr}`);

  // --- 6. Deploy TeamProofAggregator ---
  console.log("6/6 Deploying TeamProofAggregator...");
  const TeamProofAggregator = await hre.ethers.getContractFactory("TeamProofAggregator");
  const teamAgg = await TeamProofAggregator.deploy(verifierAddr);
  await teamAgg.waitForDeployment();
  const teamAggAddr = await teamAgg.getAddress();
  console.log(`     TeamProofAggregator deployed at: ${teamAggAddr}`);

  // --- 7. Grant reputation-updater roles ---
  console.log("");
  console.log("Granting reputation-updater roles...");
  const registry = await hre.ethers.getContractAt("TieredDeviceRegistry", registryAddr);

  let tx = await registry.setReputationUpdater(verifierAddr, true);
  await tx.wait();
  console.log(`  PoACVerifier (${verifierAddr}) granted updater role`);

  tx = await registry.setReputationUpdater(marketAddr, true);
  await tx.wait();
  console.log(`  BountyMarket (${marketAddr}) granted updater role`);

  // --- 8. Write deployment JSON ---
  const deployment = {
    network:   hre.network.name,
    chainId:   hre.network.config.chainId,
    deployer:  deployer.address,
    timestamp: new Date().toISOString(),
    contracts: {
      TieredDeviceRegistry: registryAddr,
      PoACVerifier:         verifierAddr,
      BountyMarket:         marketAddr,
      SkillOracle:          oracleAddr,
      ProgressAttestation:  progressAddr,
      TeamProofAggregator:  teamAggAddr,
    },
  };

  fs.mkdirSync("./deployments", { recursive: true });
  const deploymentPath = `./deployments/${hre.network.name}-core-${Date.now()}.json`;
  fs.writeFileSync(deploymentPath, JSON.stringify(deployment, null, 2));
  console.log(`Deployment info saved: ${deploymentPath}`);

  // --- 9. Write bridge/.env.testnet (core section) ---
  const rpcUrl  = hre.network.config.url || "https://babel-api.testnet.iotex.io";
  const chainId = hre.network.config.chainId || 4690;

  const envContent = [
    `# VAPI Bridge — IoTeX Testnet Core Contracts`,
    `# Auto-generated by deploy-resume-core.js — ${new Date().toISOString()}`,
    `IOTEX_RPC_URL=${rpcUrl}`,
    `IOTEX_CHAIN_ID=${chainId}`,
    `POAC_VERIFIER_ADDRESS=${verifierAddr}`,
    `BOUNTY_MARKET_ADDRESS=${marketAddr}`,
    `DEVICE_REGISTRY_ADDRESS=${registryAddr}`,
    `SKILL_ORACLE_ADDRESS=${oracleAddr}`,
    `PROGRESS_ATTESTATION_ADDRESS=${progressAddr}`,
    `TEAM_AGGREGATOR_ADDRESS=${teamAggAddr}`,
  ].join("\n") + "\n";

  const envPath = path.join(__dirname, "..", "..", "bridge", ".env.core");
  fs.writeFileSync(envPath, envContent);
  console.log(`Bridge .env.core written: ${envPath}`);

  // --- 10. Summary ---
  const remaining = await hre.ethers.provider.getBalance(deployer.address);
  console.log("");
  console.log("=== Core Deployment Complete ===");
  console.log(`Remaining balance: ${hre.ethers.formatEther(remaining)} IOTX`);
  console.log("");
  console.log("All 6 core contracts deployed. Next phases:");
  console.log("  PHG_REGISTRY_ADDRESS=" + registryAddr + " (use for phase22+)");
  console.log("  npx hardhat run scripts/deploy-phase22.js --network iotex_testnet");
}

main()
  .then(() => process.exit(0))
  .catch(err => { console.error("Resume deploy failed:", err.message); process.exit(1); });
