/**
 * contracts/scripts/deploy-verifier.js
 * Phase 15 — Deploy TeamProofVerifier.sol and wire it to TeamProofAggregatorZK.
 *
 * Prerequisites (run in order):
 *   1. cd contracts/circuits && npm install && bash setup.sh
 *        → generates contracts/contracts/TeamProofVerifier.sol
 *   2. cd contracts && npx hardhat compile
 *        → compiles TeamProofVerifier.sol
 *   3. Deploy core contracts if not already done:
 *        npx hardhat run scripts/deploy.js --network iotex_testnet
 *        (TeamProofAggregatorZK must be deployed and its address known)
 *
 * Required environment variable:
 *   TEAM_AGGREGATOR_ZK_ADDRESS   — deployed TeamProofAggregatorZK address
 *
 * Usage:
 *   npx hardhat run scripts/deploy-verifier.js --network iotex_testnet
 *
 * What this script does:
 *   1. Deploys the snarkjs-generated Groth16 verifier (TeamProofVerifier.sol)
 *   2. Calls TeamProofAggregatorZK.setTeamProofVerifier(verifierAddress)
 *      — activates the real ZK proof verification path
 *   3. Writes a deployment record to contracts/deployments/
 *   4. Appends ZK env vars to bridge/.env.testnet if it exists
 *
 * Security note:
 *   The proving key (TeamProof_final.zkey) was generated by a single-contributor
 *   Phase 2 ceremony. For mainnet, replace with a multi-party ceremony output
 *   and publish the b2sum of the final zkey for auditability.
 */

const hre  = require("hardhat");
const fs   = require("fs");
const path = require("path");

async function main() {
    const [deployer] = await hre.ethers.getSigners();
    const balance    = await hre.ethers.provider.getBalance(deployer.address);

    console.log("=== VAPI ZK Verifier Deployment (Phase 15) ===");
    console.log(`Network:  ${hre.network.name}`);
    console.log(`Deployer: ${deployer.address}`);
    console.log(`Balance:  ${hre.ethers.formatEther(balance)} IOTX`);
    console.log("");

    // ── Pre-flight: TeamProofVerifier.sol generated? ─────────────────────────
    const verifierSolPath = path.join(__dirname, "..", "contracts", "TeamProofVerifier.sol");
    if (!fs.existsSync(verifierSolPath)) {
        console.error("ERROR: contracts/contracts/TeamProofVerifier.sol not found.");
        console.error("       This file is auto-generated by setup.sh.");
        console.error("       Run: cd contracts/circuits && npm install && bash setup.sh");
        console.error("       Then: npx hardhat compile");
        process.exit(1);
    }
    console.log(`  [OK] TeamProofVerifier.sol present`);

    // ── Pre-flight: TeamProofAggregatorZK address ────────────────────────────
    const aggregatorAddr = process.env.TEAM_AGGREGATOR_ZK_ADDRESS;
    if (!aggregatorAddr || !hre.ethers.isAddress(aggregatorAddr)) {
        console.error("ERROR: TEAM_AGGREGATOR_ZK_ADDRESS env var not set or not a valid address.");
        console.error("       Set it to the deployed TeamProofAggregatorZK contract address.");
        console.error("       Example: export TEAM_AGGREGATOR_ZK_ADDRESS=0x...");
        process.exit(1);
    }
    console.log(`  [OK] TeamProofAggregatorZK: ${aggregatorAddr}`);
    console.log("");

    // ── Pre-flight: Check aggregator's current verifier state ────────────────
    let currentVerifier;
    try {
        const agg = await hre.ethers.getContractAt("TeamProofAggregatorZK", aggregatorAddr);
        currentVerifier = await agg.teamProofVerifier();
    } catch (err) {
        console.error(`ERROR: Could not read TeamProofAggregatorZK at ${aggregatorAddr}.`);
        console.error(`       Is the address correct? Network: ${hre.network.name}`);
        console.error(`       Details: ${err.message}`);
        process.exit(1);
    }
    if (currentVerifier !== "0x0000000000000000000000000000000000000000") {
        console.log(`  NOTE: teamProofVerifier is already set to ${currentVerifier}`);
        console.log("        Proceeding will overwrite it with the new deployment.");
        console.log("");
    }

    // ── Step 1: Deploy TeamProofVerifier ─────────────────────────────────────
    console.log("1/2 Deploying TeamProofVerifier (snarkjs Groth16 verifier)...");
    let zkVerifier;
    try {
        const TeamProofVerifier = await hre.ethers.getContractFactory("TeamProofVerifier");
        zkVerifier = await TeamProofVerifier.deploy();
        await zkVerifier.waitForDeployment();
    } catch (err) {
        console.error("ERROR: TeamProofVerifier deployment failed.");
        console.error("       Ensure setup.sh was run and 'npx hardhat compile' succeeded.");
        console.error(`       Details: ${err.message}`);
        process.exit(1);
    }
    const zkVerifierAddr = await zkVerifier.getAddress();
    console.log(`     TeamProofVerifier deployed at: ${zkVerifierAddr}`);

    // ── Step 2: Wire verifier to aggregator ──────────────────────────────────
    console.log(`2/2 Wiring verifier to TeamProofAggregatorZK...`);
    const aggregator = await hre.ethers.getContractAt("TeamProofAggregatorZK", aggregatorAddr);
    const tx      = await aggregator.setTeamProofVerifier(zkVerifierAddr);
    const receipt = await tx.wait();
    console.log(`     setTeamProofVerifier tx: ${receipt.hash}`);

    // Confirm
    const wiredAddr = await aggregator.teamProofVerifier();
    if (wiredAddr.toLowerCase() !== zkVerifierAddr.toLowerCase()) {
        console.error(`ERROR: teamProofVerifier mismatch: got ${wiredAddr}, expected ${zkVerifierAddr}`);
        process.exit(1);
    }
    console.log(`     Confirmed: teamProofVerifier = ${wiredAddr}`);

    // ── Summary ──────────────────────────────────────────────────────────────
    console.log("");
    console.log("=== ZK Verifier Deployment Complete ===");
    console.log("");
    console.log("Deployed addresses:");
    console.log(`  TEAM_PROOF_VERIFIER_ADDRESS  = "${zkVerifierAddr}"`);
    console.log(`  TEAM_AGGREGATOR_ZK_ADDRESS   = "${aggregatorAddr}"`);
    console.log("");
    console.log("ZK proof path is now ACTIVE.");
    console.log("  - Mock path: teamProofVerifier==address(0) OR poseidonMerkleRoot==0 [OFF]");
    console.log("  - Real path: ITeamProofVerifier.verifyProof() [ON]");
    console.log("");
    console.log("Bridge env vars to add:");
    console.log("  VAPI_ZK_WASM_PATH=bridge/zk_artifacts/TeamProof.wasm");
    console.log("  VAPI_ZK_ZKEY_PATH=bridge/zk_artifacts/TeamProof_final.zkey  # KEEP SECRET");
    console.log("  VAPI_ZK_VKEY_PATH=bridge/zk_artifacts/verification_key.json");
    console.log("");
    console.log("Submit proofs via 7-param submitTeamProofZK() with poseidonMerkleRoot != 0.");
    console.log("");

    // ── Write deployment JSON ─────────────────────────────────────────────────
    const deploymentsDir = path.join(__dirname, "..", "deployments");
    fs.mkdirSync(deploymentsDir, { recursive: true });
    const timestamp      = Date.now();
    const deploymentPath = path.join(
        deploymentsDir,
        `zk-verifier-${hre.network.name}-${timestamp}.json`
    );
    const record = {
        network:              hre.network.name,
        chainId:              hre.network.config.chainId,
        deployer:             deployer.address,
        timestamp:            new Date(timestamp).toISOString(),
        TeamProofVerifier:    zkVerifierAddr,
        TeamProofAggregatorZK: aggregatorAddr,
        setVerifierTxHash:    receipt.hash,
        phase:                "15",
    };
    fs.writeFileSync(deploymentPath, JSON.stringify(record, null, 2));
    console.log(`Deployment record: ${deploymentPath}`);

    // ── Append to bridge/.env.testnet ─────────────────────────────────────────
    const envPath = path.join(__dirname, "..", "..", "bridge", ".env.testnet");
    if (fs.existsSync(envPath)) {
        const zkBlock = [
            "",
            "# -- ZK Proof Setup (Phase 15) ----------------------------------------",
            `TEAM_PROOF_VERIFIER_ADDRESS=${zkVerifierAddr}`,
            "# After copying artifacts from contracts/circuits/ to bridge/zk_artifacts/:",
            "# VAPI_ZK_WASM_PATH=bridge/zk_artifacts/TeamProof.wasm",
            "# VAPI_ZK_ZKEY_PATH=bridge/zk_artifacts/TeamProof_final.zkey",
            "# VAPI_ZK_VKEY_PATH=bridge/zk_artifacts/verification_key.json",
        ].join("\n");
        fs.appendFileSync(envPath, zkBlock + "\n");
        console.log(`ZK vars appended to: ${envPath}`);
    }
}

main()
    .then(() => process.exit(0))
    .catch(err => {
        console.error("Deployment failed:", err);
        process.exit(1);
    });
